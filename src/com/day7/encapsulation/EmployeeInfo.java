package com.day7.encapsulation;

public class EmployeeInfo {
	
	
	private String empFname = "James";
	private String empLname = "John";
	private int empAge = 34;
	private double empSalary = 2000;
	private double empBonus = 300;
	
	
	public String getEmpFname() {
		return empFname;
	}
	public String getEmpLname() {
		return empLname;
	}
	public int getEmpAge() {
		return empAge;
	}
	public double getEmpSalary() {
		return empSalary;
	}
	public double getEmpBonus() {
		return empBonus;
	}
	
	
	
	
	

}
